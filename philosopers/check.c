#include "philo.h"

int	check_argoments(int ac, char **av)
{
	int	i;
	int t;

	i = 1;
	while (i != ac)
	{
		t = 0;
		while(av[i][t])
		{
			if(!(av[i][t] == ' ' || (av[i][t] >= '0' && av[i][t] <= '9')))
				return (1);
			t++;
		}
		i++;
	}
	return (0);
}